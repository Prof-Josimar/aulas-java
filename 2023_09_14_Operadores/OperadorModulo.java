package br.com.view;

/*

O operador de módulo % retorna o resto da divisão e não o resultado. 
Para relembrar das aulas de matemática, 
o resto é o valor que sobra em uma divisão para que o quociente/resultado continue sendo um inteiro.
Por exemplo, a divisão entre 10 e 3 terá quociente 3 e resto 1, como mostra a imagem abaixo.

 */
public class OperadorModulo {

    public static void main(String[] args) {
        float f =  10 / 3;
        System.out.println(f);
        System.out.println(10 % 2);

    }

}
